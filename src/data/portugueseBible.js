// Função para buscar versículo em português nos dados locais
export function getLocalPortugueseVerse(reference) {
  const normalizedRef = reference.toLowerCase().replace(/\s+/g, ' ')
  return PORTUGUESE_VERSES[normalizedRef] || null
}

// Função para obter lista de versículos disponíveis em português
export function getAvailablePortugueseVerses() {
  return Object.keys(PORTUGUESE_VERSES)
}

// Função para buscar por texto em português
export function searchPortugueseVerses(query) {
  const results = []
  const searchTerm = query.toLowerCase()
  
  Object.entries(PORTUGUESE_VERSES).forEach(([, data]) => {
    data.verses.forEach(verse => {
      if (verse.text.toLowerCase().includes(searchTerm)) {
        results.push({
          reference: data.reference,
          verse: verse,
          match: verse.text
        })
      }
    })
  })
  
  return results
}
